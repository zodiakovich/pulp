/**
 * Local artist → musical style mapping. Keys are normalized (lowercase, no accents).
 * Artist strings from user input must not be persisted or sent to third parties when
 * resolved locally; use the derived descriptors only downstream.
 */

export interface ArtistProfile {
  genres: string[];
  bpm_range: [number, number];
  energy: 'low' | 'medium' | 'high';
  density: 'sparse' | 'normal' | 'dense';
  mood_tags: string[];
  swing: boolean;
}

/** NFD strip combining marks */
export function normalizeArtistKey(input: string): string {
  return input
    .normalize('NFD')
    .replace(/\p{M}/gu, '')
    .toLowerCase()
    .trim();
}

/** Build case- and accent-insensitive regex for a normalized ascii key */
function keyToFlexiblePattern(key: string): RegExp {
  const parts = key.split('').map((ch) => {
    if (/[a-z0-9]/.test(ch)) {
      return `${ch}\\p{M}*`;
    }
    if (ch === ' ') {
      return '\\s+';
    }
    return '\\' + ch;
  });
  return new RegExp(`(?:^|\\b|[^a-z0-9])(${parts.join('')})(?:$|\\b|[^a-z0-9])`, 'giu');
}

export function stripArtistKeyFromPrompt(prompt: string, matchedKey: string): string {
  const re = keyToFlexiblePattern(matchedKey);
  return prompt.replace(re, ' ').replace(/\s+/g, ' ').trim();
}

export function profileToDescriptorSuffix(p: ArtistProfile): string {
  const mood = p.mood_tags.join(' ');
  const genreLine = p.genres.join(' ');
  const swingBit = p.swing ? ' with swing' : '';
  return `${mood} ${genreLine}${swingBit}`.replace(/\s+/g, ' ').trim();
}

