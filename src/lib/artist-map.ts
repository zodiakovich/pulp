function normalizeInnerSpaces(s: string): string {
  return s.replace(/\s+/g, ' ').trim();
}

/** Word-boundary style: padded spaces so substrings of words do not match. */
export function findArtistKeyInNormalizedPrompt(normPrompt: string): string | null {
  const padded = ` ${normalizeInnerSpaces(normPrompt)} `;
  const keys = Object.keys(ARTIST_MAP).sort((a, b) => b.length - a.length);
  for (const key of keys) {
    const needle = ` ${normalizeInnerSpaces(key)} `;
    if (padded.includes(needle)) return key;
  }
  return null;
}

/**
 * Resolve a string (prompt fragment or full prompt) to a profile: exact key,
 * then longest substring match in normalized space.
 */
export function resolveArtistToParams(input: string): ArtistProfile | null {
  const norm = normalizeArtistKey(input);
  if (!norm) return null;
  if (ARTIST_MAP[norm]) return ARTIST_MAP[norm];
  const key = findArtistKeyInNormalizedPrompt(norm);
  if (key) return ARTIST_MAP[key];
  return null;
}

export function findArtistInPrompt(prompt: string): { matchedKey: string; profile: ArtistProfile } | null {
  const norm = normalizeArtistKey(prompt);
  const key = findArtistKeyInNormalizedPrompt(norm);
  if (!key) return null;
  return { matchedKey: key, profile: ARTIST_MAP[key]! };
}
